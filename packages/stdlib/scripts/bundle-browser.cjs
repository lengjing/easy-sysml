/**
 * bundle-browser.cjs
 *
 * Reads all SysML/KerML standard library files from lib/
 * and generates a TypeScript module with their content as string literals.
 *
 * Output: src/generated/browser-bundle.ts
 */
const fs = require('fs');
const path = require('path');

const libDir = path.resolve(__dirname, '../lib');
const outDir = path.resolve(__dirname, '../src/generated');
const outFile = path.join(outDir, 'browser-bundle.ts');

if (!fs.existsSync(libDir)) {
  console.error(`[bundle-browser] stdlib lib/ directory not found at: ${libDir}`);
  process.exit(1);
}

const files = fs.readdirSync(libDir)
  .filter(f => f.endsWith('.kerml') || f.endsWith('.sysml'))
  .sort();

if (files.length === 0) {
  console.error('[bundle-browser] No .kerml/.sysml files found');
  process.exit(1);
}

fs.mkdirSync(outDir, { recursive: true });

const entries = files.map(file => {
  const content = fs.readFileSync(path.join(libDir, file), 'utf-8');
  return `  ${JSON.stringify(file)}: ${JSON.stringify(content)}`;
});

const output = [
  '// @ts-nocheck',
  '// Auto-generated by scripts/bundle-browser.cjs — do not edit',
  `// ${files.length} stdlib files bundled at ${new Date().toISOString()}`,
  '',
  'export const STDLIB_FILES: Record<string, string> = {',
  entries.join(',\n'),
  '};',
  '',
].join('\n');

fs.writeFileSync(outFile, output, 'utf-8');
console.log(`[bundle-browser] Generated ${outFile} with ${files.length} files`);
