/**
 * bundle-browser.cjs
 *
 * Reads all SysML/KerML standard library files from lib/
 * and generates a JavaScript module with their content as string literals.
 *
 * Output:
 *   dist/stdlib-browser-bundle.js   — runtime module (loaded by browser.ts)
 *   dist/stdlib-browser-bundle.d.ts — type declarations (for downstream consumers)
 */
const fs = require('fs');
const path = require('path');

const libDir = path.resolve(__dirname, '../lib');
const outDir = path.resolve(__dirname, '../dist');

if (!fs.existsSync(libDir)) {
  console.error(`[bundle-browser] stdlib lib/ directory not found at: ${libDir}`);
  process.exit(1);
}

const files = fs.readdirSync(libDir)
  .filter(f => f.endsWith('.kerml') || f.endsWith('.sysml'))
  .sort();

if (files.length === 0) {
  console.error('[bundle-browser] No .kerml/.sysml files found');
  process.exit(1);
}

fs.mkdirSync(outDir, { recursive: true });

const entries = files.map(file => {
  const content = fs.readFileSync(path.join(libDir, file), 'utf-8');
  return `  ${JSON.stringify(file)}: ${JSON.stringify(content)}`;
});

// Generate .js module (ES module)
const jsOutput = [
  '// Auto-generated by scripts/bundle-browser.cjs — do not edit',
  `// ${files.length} stdlib files bundled at ${new Date().toISOString()}`,
  '',
  'export const STDLIB_FILES = {',
  entries.join(',\n'),
  '};',
  '',
].join('\n');

const jsFile = path.join(outDir, 'stdlib-browser-bundle.js');
fs.writeFileSync(jsFile, jsOutput, 'utf-8');

// Generate .d.ts declaration (next to .js in dist/)
const dtsOutput = [
  '// Auto-generated by scripts/bundle-browser.cjs — do not edit',
  'export declare const STDLIB_FILES: Record<string, string>;',
  '',
].join('\n');

const dtsFile = path.join(outDir, 'stdlib-browser-bundle.d.ts');
fs.writeFileSync(dtsFile, dtsOutput, 'utf-8');

console.log(`[bundle-browser] Generated ${jsFile} with ${files.length} files`);
